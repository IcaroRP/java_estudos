package processamentoDados;

public class ExemploQuatro {

	public static void main(String[] args) {
		//Declarando variaveis
		int a, b;
		double resultado;
		
		//Processando dados
		a = 5;
		b = 2;
		
		//avisando que quero um numero double (casting)
		resultado = (double) a / b;
		
		//Informando dados
		System.out.println(resultado);

	}

}
